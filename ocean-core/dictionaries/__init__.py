def detect_language_hint(text: str) -> str:
    """Detect language based on common words"""
    text_lower = text.lower()
    
    # Albanian indicators
    albanian_words = ["pershendetje", "faleminderit", "shqip", "kush", "cfare", "ku", "pse", "jam", "jeni", "eshte", "te lutem", "mire"]
    for word in albanian_words:
        if word in text_lower:
            return "albanian"
    
    # German indicators
    if any(w in text_lower for w in ["guten", "danke", "bitte", "wie", "ich bin", "verstehe"]):
        return "german"
    
    # Italian indicators
    if any(w in text_lower for w in ["buongiorno", "grazie", "prego", "sono", "capisco"]):
        return "italian"
    
    # French indicators
    if any(w in text_lower for w in ["bonjour", "merci", "je suis", "comprends"]):
        return "french"
    
    # Spanish indicators
    if any(w in text_lower for w in ["hola", "gracias", "como esta", "entiendo"]):
        return "spanish"
    
    return "english"
